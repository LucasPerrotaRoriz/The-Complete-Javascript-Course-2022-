/**
The flat() method is an inbuilt array method that flattens a given array into a newly
created one-dimensional array. It concatenates all the elements of the given multidimensional
array, and flats up to the specified depth.
The flat method only goes one level deep when flattening the array. That can be fixed by defining
the depth.

The flatMap method combines a map and flat method into one method which is better for performance.
flatMap also does mapping so it needs to receive the same callback as a map method. flatMap
only goes one level deep and it cannot be changed. If there is the need to go more levels
deep the flat method be used.

*/

const arr = [[1, 2, 3], [4, 5, 6], 7, 8];
console.log(arr.flat(1));

const arrDeep = [[[1, 2], 3], [4, [5, 6]], 7, 8];
console.log(arrDeep.flat(2));

