// Slider
const slider = function () {
  const slides = document.querySelectorAll('.slide');
  const btnLeft = document.querySelector('.slider__btn--left');
  const btnRight = document.querySelector('.slider__btn--right');
  const dotContainer = document.querySelector('.dots');

  let curSlide = 0;
  const maxSlide = slides.length;

  // Functions
  const createDots = function () {
    slides.forEach(function (_, i) {
      dotContainer.insertAdjacentHTML(
        'beforeend',
        `<button class="dots__dot--${i}" data-slide="${i}"></button>`
      );
    });
  };

  const activateDot = function (slide) {
    document
      .querySelectorAll('.dots__dot')
      .forEach(dot => dot.classList.remove('dots__dot--active'));

    document
      .querySelector(`.dots__dot[data-slide="${slide}"]`)
      .classList.add('dots__dot--active');
  };

  const goToSlide = function (slide) {
    slides.forEach(
      (s, i) => (s.style.transform = `translateX(${100 * (i - slide)}%)`)
    );
  };

  // Next slide
  const nextSlide = function () {
    if (curSlide === maxSlide - 1) {
      curSlide = 0;
    } else {
      curSlide++;
    }

    goToSlide(curSlide);
    activateDot(curSlide);
  };

  const prevSlide = function () {
    if (curSlide === 0) {
      curSlide = maxSlide - 1;
    } else {
      curSlide--;
    }
    goToSlide(curSlide);
    activateDot(curSlide);
  };

  const init = function () {
    goToSlide(0);
    createDots();

    activateDot(0);
  };
  init();

  // Event handlers
  btnRight.addEventListener('click', nextSlide);
  btnLeft.addEventListener('click', prevSlide);

  document.addEventListener('keydown', function (e) {
    if (e.key === 'ArrowLeft') prevSlide();
    e.key === 'ArrowRight' && nextSlide();
  });

  dotContainer.addEventListener('click', function (e) {
    if (e.target.classList.contains('dots__dot')) {
      const { slide } = e.target.dataset;
      goToSlide(slide);
      activateDot(slide);
    }
  });
};
slider();

/*
Defer and async change the way JS is fetched, which means downloaded and then executed.

When the script is included without any attribute in the head what will the page loading process
look like over time?

As the user loads the page and receives the HTML, the HTML code will start
to be parsed by the browser and parsing the HTML is basically building the DOM tree from the HTML
elements. At some point it will find the script tag, start to fetch the script and then execute it.
During all this time the HTML parsing will stop, it will be waiting for the script to get fetched and
executed. Only after that the rest of the HTML can be parsed and at the end of the parsing the
DOM content loaded event will get fired.

This is not ideal because the browser does nothing for some time, impacting the page performance.
The script will also get executed before the DOM is ready. By putting the script tag at the end of
body the HTML gets parsed, the script gets fetched and executed. This is still not good because
the script could have been download before while the HTML is still being parsed. Use the script tag
at the end of the body if you want to support older browsers, since async and defer work in newer browsers.

When you use the Async attribute in the head of the document the difference is that the script is
loaded at the same time as the HTML is parsed, in a asynchronous way. However the HTML parsing
still stops for the script execution. The script is download asynchronously but then is executed
right away in a synchronous way, so the HTML has to wait for being parsed. This makes the page
loading time shorter.

When you use the defer attribute in the head the script is still loaded asynchronously, but the
execution of the script is deferred until the end of the HTML parsing. Loading time is similar to
the sync attribute but the difference is that with defer the HTML parsing is never interrupted,
because the script is only executed at the end.

Async and defer on the body do not make sense.

// Comparing async and defer

 Async
- Scripts are fetched asynchronously and executed immediately.
- Usually the DOMContentLoaded event waits for all scripts to execute, except for async scripts.
So DOMContentLoaded does not wait for an async script.
- Scripts not guaranteed to execute in order
- Use for 3rd-party scripts where order doesn't matter(e.g Google Analytics)

 Defer
- Scripts are fetched asynchronously and executed after the HTML is completely parsed.
- DOMContentLoaded event fires after defer script is executed.
- Scripts are executed in order
- This is overall the best solution! Use for your own scripts, and when order matters
(e.g. including a library)

*/
